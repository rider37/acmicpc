n = int(input())
m = 2**(n+1) - 3
a = ["*"]
for i in range(2, n+1):
    if i % 2 == 0:
        for j in range(0, 2**i-3):
            if j < (2**(i-1))-1:
                a[j] = " "*(j+1) + "*" + " "*(2**(i-1)-2-j) + \
                    a[j] + " "*(2*(2**(i-1)-2-j)) + "*"
            else:
                a.append(" "*(j+1) + "*" + " " *
                         (2**i-5-2*(j-(2**(i-1)-1))) + "*")
        a.append(" "*((2**(i+1)-3)//2) + "*")
        a.insert(0, "*"*(2**(i+1)-3))
    else:
        for j in range(0, 2**i-3):
            if j < 2**(i-1)-1:
                a[j] = " "*(((2**i-2)//2)-j) + "*" + \
                    " "*j + a[j] + " "*(2*j) + "*"
            else:
                a.insert(0, " "*(j+1) + "*" + " " *
                         (2**i-2*(j-2**(i-1)+3)-1) + "*")
        a.insert(0, " "*((2**(i+1)-3)//2) + "*")
        a.append("*"*(2**(i+1)-3))
for i in range(2**n-1):
    print(a[i])
